	LIST P=16F84		
              #include "P16F84.INC"
              __CONFIG _WDT_OFF &_XT_OSC &_PWRTE_ON &_CP_OFF
LSB 	EQU 	H'20'
MSB 	EQU 	H'21'
;**************
	ORG 	0X00
	GOTO 	BASLA
;*******************
BEKLE
	MOVLW .45
	MOVWF LSB
DON1
	MOVLW .95
	MOVWF MSB
DON2	
	DECFSZ  MSB,F
	GOTO DON2
	DECFSZ  LSB,F
	GOTO DON1
	RETURN
;****************

BASLA
	CLRF	 MSB
	CLRF	 LSB
	BSF	 STATUS,5
	MOVLW 	 B'11111111'
	MOVWF    TRISA	
	CLRF	 TRISB
	BCF	 STATUS,5	
	CLRF 	PORTB
;********************************************	
TEST
	BTFSS PORTA,0
	GOTO    SAG
	BTFSS PORTA,1
	GOTO	SOL
	GOTO   TEST

SOL	
	BTFSS PORTA,2
	GOTO    TEST
	
	MOVLW B'11000110'
	MOVWF PORTB
	CALL BEKLE

	MOVLW B'01000011'
	MOVWF PORTB
	CALL BEKLE

	MOVLW B'01001001'
	MOVWF PORTB
	CALL BEKLE

	MOVLW B'11001100'
	MOVWF PORTB
	CALL BEKLE
	CLRF 	PORTB
	BTFSS PORTA,0
	GOTO      SAG
	GOTO      SOL
;*******************************
SAG
	BTFSS PORTA,2
	GOTO    TEST
	
	MOVLW B'00110011'
	MOVWF PORTB
	CALL BEKLE
	
	MOVLW B'00100110'
	MOVWF PORTB
	CALL BEKLE

	MOVLW B'00101100'
	MOVWF PORTB
	CALL BEKLE

	MOVLW B'00111001'
	MOVWF PORTB
	CALL BEKLE
	CLRF 	PORTB
	BTFSS PORTA,1
	GOTO	 SOL
	GOTO	 SAG

	END

